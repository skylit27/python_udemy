operandoA = 3
operandoB = 2

# Suma
suma = operandoA + operandoB
print("Resultado suma:", suma)
print(f"Resultado suma: {suma}")

# Resta

resta = operandoA - operandoB
print(f"resultado resta: {resta}")

# multiplicacion

multiplicacion = operandoA * operandoB
print(f"resultado multiplicacion: {multiplicacion}")

# division double

division = operandoA / operandoB
print(f"resultado division double: {division}")

# division int

division2 = operandoA // operandoB
print(f"resultado division int: {division2}")

# modulo

modulo = operandoA % operandoB
print(f"resultado operador: {modulo}")

# Exponente


exponente = operandoA ** operandoB
print(f"resultado operador: {exponente}")


